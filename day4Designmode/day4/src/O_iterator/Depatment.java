public class Depatment {
    private  String name;
    private  String dest;

    public Depatment(String name, String dest) {
        this.name = name;
        this.dest = dest;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDest() {
        return dest;
    }

    public void setDest(String dest) {
        this.dest = dest;
    }
}
